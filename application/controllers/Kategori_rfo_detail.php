class Kategori_rfo_detail extends CI_Controller
{
    function __construct()
    {
        parent::__construct();
        //is_login();
        $this->load->model('Kategori_rfo_detail_model');
        $this->load->library('form_validation');        
	$this->load->library('datatables');
    }

    public function index()
    {
        $this->template->load('template','kategori_rfo_detail/tbl_kategori_rfo_detail_list');
    } 
    
    public function json() {
        header('Content-Type: application/json');
        echo $this->Kategori_rfo_detail_model->json();
    }

    public function read($id) 
    {
        $row = $this->Kategori_rfo_detail_model->get_by_id($id);
        if ($row) {
            $data = array(
		'id_tbl_kategori_rfo_detail' => $row->id_tbl_kategori_rfo_detail,
		'nama_kategori_rfo_detail' => $row->nama_kategori_rfo_detail,
	    );
            $this->template->load('template','kategori_rfo_detail/tbl_kategori_rfo_detail_read', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('kategori_rfo_detail'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Create',
            'action' => site_url('kategori_rfo_detail/create_action'),
	    'id_tbl_kategori_rfo_detail' => set_value('id_tbl_kategori_rfo_detail'),
	    'nama_kategori_rfo_detail' => set_value('nama_kategori_rfo_detail'),
	);
        $this->template->load('template','kategori_rfo_detail/tbl_kategori_rfo_detail_form', $data);
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'nama_kategori_rfo_detail' => $this->input->post('nama_kategori_rfo_detail',TRUE),
	    );

            $this->Kategori_rfo_detail_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success 2');
            redirect(site_url('kategori_rfo_detail'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->Kategori_rfo_detail_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('kategori_rfo_detail/update_action'),
		'id_tbl_kategori_rfo_detail' => set_value('id_tbl_kategori_rfo_detail', $row->id_tbl_kategori_rfo_detail),
		'nama_kategori_rfo_detail' => set_value('nama_kategori_rfo_detail', $row->nama_kategori_rfo_detail),
	    );
            $this->template->load('template','kategori_rfo_detail/tbl_kategori_rfo_detail_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('kategori_rfo_detail'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('id_tbl_kategori_rfo_detail', TRUE));
        } else {
            $data = array(
		'nama_kategori_rfo_detail' => $this->input->post('nama_kategori_rfo_detail',TRUE),
	    );

            $this->Kategori_rfo_detail_model->update($this->input->post('id_tbl_kategori_rfo_detail', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('kategori_rfo_detail'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->Kategori_rfo_detail_model->get_by_id($id);

        if ($row) {
            $this->Kategori_rfo_detail_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('kategori_rfo_detail'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('kategori_rfo_detail'));
        }
    }

    public function _rules() 
    {
	$this->form_validation->set_rules('nama_kategori_rfo_detail', 'nama kategori rfo detail', 'trim|required');

	$this->form_validation->set_rules('id_tbl_kategori_rfo_detail', 'id_tbl_kategori_rfo_detail', 'trim');
	$this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

}

